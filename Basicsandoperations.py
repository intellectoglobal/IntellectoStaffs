#BACKSLASH
#IN PYTHON THE BACKSLASH \ IS A SPECIAL CHARACTER CALLED THE ESCAPE CHARACTER
# IT IS USED TO REPRESENT CERTAINN THINGS IN A STRING SUCH AS NEW LINES , TABS AND OTHER USEFULL THINGS

print('Brian\'s mother : He\'s not an angel.He\'s a very naughty boy!')
#output : Brian's mother : He's not an angel.He's a very naughty boy!

print("He said:\"cool!\".")
#output : He said:"cool!".

#NEW LINES
#TO CREATE A NEW LINE WE USE \n
print('One \nTwo \nThree')
#output : 
# One 
# Two
# Three

#TAB
# TO CREATE A TAB WE USE \t
print("\t hey \t there")
#output : hey   there

#CARRIAGE RETURN
# \r is the carriage return symbol
print("Hi \r there")
#output : there

#ESCAPING
#IF WE NEED SPECIAL CHARACTER \n IN THE OUTPUT ,WE CAN ESCAPE IT USING ANOTHER BACKSLASH \\n
print("new line is \\n")
#output : new line is \n

print('\\r\\n')
#output:\r\n

#Hint \r\n for windows user & \n for Linux or unix


